a = 10
b = 5
print("Arithmetic Operators:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)

print("\nRelational Operators:")
print("a > b:", a > b)
print("a < b:", a < b)

print("\nLogical Operators:")
print("a > 5 and b < 10:", a > 5 and b < 10)
print("a < 5 or b < 10:", a < 5 or b < 10)

print("\nAssignment Operators:")
a += 1
print("a += 1:", a)

print("\nBitwise Operators:")
print("a & b:", a & b)
print("a | b:", a | b)